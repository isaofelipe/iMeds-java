package controle.areaInicial;

/**
 *
 * @author alunodev06
 */
public class TelaInicialControle {
    public String login;
    public String senha;

    public TelaInicialControle(String login, String senha) {
        this.login = login;
        this.senha = senha;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
}
